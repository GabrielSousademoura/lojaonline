CREATE TABLE IF NOT EXISTS public.marketplace_pedido (
    id_pedido BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    codigo_pedido VARCHAR(40) NOT NULL UNIQUE,
    id_comprador BIGINT NOT NULL,
    id_vendedor BIGINT NOT NULL,
    id_endereco_entrega BIGINT,
    status_pedido VARCHAR(30) NOT NULL DEFAULT 'criado',
    subtotal DECIMAL(12, 2) NOT NULL DEFAULT 0.00,
    valor_frete DECIMAL(12, 2) NOT NULL DEFAULT 0.00,
    valor_desconto DECIMAL(12, 2) NOT NULL DEFAULT 0.00,
    valor_taxa_plataforma DECIMAL(12, 2) NOT NULL DEFAULT 0.00,
    total DECIMAL(12, 2) NOT NULL DEFAULT 0.00,
    observacao TEXT,
    data_pedido TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    data_atualizacao TIMESTAMP NULL DEFAULT NULL,

    CONSTRAINT chk_marketplace_pedido_status
        CHECK (status_pedido IN (
            'criado',
            'aguardando_pagamento',
            'pago',
            'em_preparacao',
            'enviado',
            'entregue',
            'concluido',
            'cancelado',
            'reembolsado',
            'disputa'
        )),

    CONSTRAINT chk_marketplace_pedido_usuarios_diferentes
        CHECK (id_comprador <> id_vendedor),

    CONSTRAINT chk_marketplace_pedido_valores
        CHECK (
            subtotal >= 0
            AND valor_frete >= 0
            AND valor_desconto >= 0
            AND valor_taxa_plataforma >= 0
            AND total >= 0
        ),

    CONSTRAINT fk_marketplace_pedido_comprador
        FOREIGN KEY (id_comprador)
        REFERENCES public.admcore_cliente(id_cliente)
        ON DELETE RESTRICT
        ON UPDATE CASCADE,

    CONSTRAINT fk_marketplace_pedido_vendedor
        FOREIGN KEY (id_vendedor)
        REFERENCES public.admcore_cliente(id_cliente)
        ON DELETE RESTRICT
        ON UPDATE CASCADE,

    CONSTRAINT fk_marketplace_pedido_endereco_entrega
        FOREIGN KEY (id_endereco_entrega)
        REFERENCES public.admcore_endereco(id_endereco)
        ON DELETE SET NULL
        ON UPDATE CASCADE
);

